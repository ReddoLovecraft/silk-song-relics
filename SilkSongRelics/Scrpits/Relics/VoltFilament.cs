namespace SilkSongRelics.Scrpits.Relics
{
[Pool(typeof(SharedRelicPool))]
public class VoltFilament : SilkSongReic
{
    public override RelicRarity Rarity => RelicRarity.Rare;
	protected override IEnumerable<DynamicVar> CanonicalVars => (new DynamicVar[1]
	{
		new DamageVar(6m, ValueProp.Unpowered)
	});
   	public override async Task AfterCardPlayed(PlayerChoiceContext context, CardPlay cardPlay)
	{
		if (cardPlay.Card.Owner == base.Owner && CombatManager.Instance.IsInProgress && cardPlay.Card.Type == CardType.Skill)
		{
			Flash();
			Creature creature = base.Owner.RunState.Rng.CombatTargets.NextItem(base.Owner.Creature.CombatState.HittableEnemies);
			if (creature != null)
			{
			VfxCmd.PlayOnCreatureCenter(creature, "vfx/vfx_attack_blunt");
			await CreatureCmd.Damage(context, creature, base.DynamicVars.Damage, base.Owner.Creature);
			}
		}
	}
}
}
